package com.support;

public class Caller {
	 private String callerId;
	 private String costomerID;
	 private String callerSearchInfo;
	 private String callerSentInfo;
	 private String sentInfoUserId;
	 private String callDuration;
	 private String empId;
	public String getCallerId() {
		return callerId;
	}
	public void setCallerId(String callerId) {
		this.callerId = callerId;
	}
	public String getCostomerID() {
		return costomerID;
	}
	public void setCostomerID(String costomerID) {
		this.costomerID = costomerID;
	}
	public String getCallerSearchInfo() {
		return callerSearchInfo;
	}
	public void setCallerSearchInfo(String callerSearchInfo) {
		this.callerSearchInfo = callerSearchInfo;
	}
	public String getCallerSentInfo() {
		return callerSentInfo;
	}
	public void setCallerSentInfo(String callerSentInfo) {
		this.callerSentInfo = callerSentInfo;
	}
	public String getSentInfoUserId() {
		return sentInfoUserId;
	}
	public void setSentInfoUserId(String sentInfoUserId) {
		this.sentInfoUserId = sentInfoUserId;
	}
	public String getCallDuration() {
		return callDuration;
	}
	public void setCallDuration(String callDuration) {
		this.callDuration = callDuration;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	 
}
